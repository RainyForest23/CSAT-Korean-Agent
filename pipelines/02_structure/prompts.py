"""
구조화 LLM 프롬프트

교재 OCR 텍스트에서 (지문/문제/선지/정답/해설)을 추출하는 프롬프트.
"""

SYSTEM_PROMPT = """당신은 수능 국어 교재 텍스트를 구조화하는 전문가입니다.
OCR로 추출된 교재 텍스트를 분석하여 정해진 JSON 형식으로 변환하세요.

교재 텍스트의 특징:
- 지문 앞에는 보통 "다음 글을 읽고 물음에 답하시오" 또는 "[○~○] 다음 글을 읽고..." 형식
- 문제 번호는 숫자(1, 2, 3...) 또는 괄호([1], [2]...) 형식
- 선지는 ①②③④⑤ 또는 (1)(2)(3)(4)(5) 형식
- 해설은 "해설", "풀이", "정답과 해설" 등의 구분자 이후에 등장
- 장르: 현대시 / 현대소설 / 고전시가 / 고전소설 / 독서(비문학)

반드시 아래 JSON 형식으로만 출력하세요. 다른 텍스트 없이."""

EXTRACTION_TEMPLATE = """다음은 수능 국어 교재의 OCR 텍스트입니다. 구조화하세요.

[OCR 텍스트]
{ocr_text}

[출력 형식]
{{
  "genre": "현대시|현대소설|고전시가|고전소설|독서",
  "passage": "지문 전문 (없으면 null)",
  "questions": [
    {{
      "number": 1,
      "stem": "문제 본문",
      "options": ["①선지1", "②선지2", "③선지3", "④선지4", "⑤선지5"],
      "answer": "정답 번호 (없으면 null)",
      "commentary": "해설 전문 (없으면 null)"
    }}
  ]
}}"""


def build_prompt(ocr_text: str) -> str:
    return EXTRACTION_TEMPLATE.format(ocr_text=ocr_text[:4000])  # 토큰 제한 방지
